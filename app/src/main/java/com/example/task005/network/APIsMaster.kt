package com.example.task005.network

import android.content.SharedPreferences
import com.example.task005.di.NetworkModule
import com.example.task005.utils.SuperRepository
import com.example.task005.utils.SuperRepositoryCallback
import com.example.task005.utils.logutil.LogUtil
import java.util.*

abstract class APIsMaster(
    private val pref: SharedPreferences,
    private val logUtil: LogUtil,
    private val baseUrl: String,
) : SuperRepository() {

    abstract fun restartApp()
    abstract fun mayUseLogsForServer(log: String)

    init {

        registerForUnAuthorisedGlobalCallback(
            object : SuperRepositoryCallback<com.example.task005.models.core.Result> {

                override fun notAuthorised() {
                    super.notAuthorised()
                }
            }
        )

        super.logUtilForSuper = logUtil
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////
    //private vars
    protected val apiInterface: ApiInterface = NetworkModule(logUtil, baseUrl).apiInterface
    ////////////////////////////////////////////////////////////////////////////////////////////////

    ////////////////////////////////////////////////////////////////////////////////////////////////
    //Api Calls
    ////////////////////////////////////////////////////////////////////////////////////////////////

    ////////////////////////////////////////////////////////////////////////////////////////////////
    //Public methods
    ////////////////////////////////////////////////////////////////////////////////////////////////

    ////////////////////////////////////////////////////////////////////////////////////////////////
    //Private Methods
    ////////////////////////////////////////////////////////////////////////////////////////////////
}